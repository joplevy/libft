#include "libft.h"

int		ft_isprime(int n)
{
	int i;

	i = 3;
	if (n == 2)
		return (1);
	if (n % 2 == 0)
		return (0);
	while ((i < n) && (n % i != 0))
		i += 2;
	if (i == n)
		return (1);
	return (0);
}
